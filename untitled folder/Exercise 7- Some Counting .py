# USE WHILE LOOP AND ASSIGNMENT OPERATION TO CREATE A LOOP THAT COUNTS FROM 1 - 50
i = 0
while i <= 50:
    print(i)
    i += 1
    
else:
    print("Loop Completed") # PRINT THE RESULT WITH THE MESSAGE LOOP COMPLETED

# USE WHILE LOOP AND ASSIGNMENT OPERATION TO CREATE A LOOP THAT COUNTS DOWN FROM 50 - 1
i = 50
while i >= 0:
    print(i)
    i -= 1
    
else:
    print("Loop Completed") # PRINT THE RESULT WITH THE MESSAGE LOOP COMPLETED

